package neo4j;

public class filmT {
	String id;
	String name;
	int releaseDate;
	String director;
	int price;
	
	
	public filmT(String id, String name, int releaseDate, String director, int price) {
		super();
		this.id = id;
		this.name = name;
		this.releaseDate = releaseDate;
		this.director = director;
		this.price = price;
	}
}
